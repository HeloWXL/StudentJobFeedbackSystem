package com.qztc.demo.model;

public class RankSumScore {
    private Integer rankSumScoreId;

    private Integer sumScoreId;

    private Integer rankSumScore;

    public Integer getRankSumScoreId() {
        return rankSumScoreId;
    }

    public void setRankSumScoreId(Integer rankSumScoreId) {
        this.rankSumScoreId = rankSumScoreId;
    }

    public Integer getSumScoreId() {
        return sumScoreId;
    }

    public void setSumScoreId(Integer sumScoreId) {
        this.sumScoreId = sumScoreId;
    }

    public Integer getRankSumScore() {
        return rankSumScore;
    }

    public void setRankSumScore(Integer rankSumScore) {
        this.rankSumScore = rankSumScore;
    }
}